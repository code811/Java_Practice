class Date {

    private int month;
    private int days;
    private int year;

    public Date(int month, int days, int year) {
        this.month = month;
        this.days = days;
        this.year = year;
    }

    public int getMonth() {
        return month;
    }

    public int getDays() {
        return days;
    }

    public int getYear() {
        return year;
    }
}
